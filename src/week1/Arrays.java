package week1;

public class Arrays {
	
	public static void main(String args[]) {
		String[] flowers = {"roses", "sunflowers", "carnations"};
		
		// print array length
		System.out.println(flowers.length);
		
		// loop through array
		for(int i=0;i<flowers.length;i++) {
			System.out.println(flowers[i]);
		}
	}


}
